import pandas as pd

# 예제 데이터프레임
df = pd.DataFrame({
    '날짜': pd.date_range('2025-10-28', periods=5),
    '매출': [100, 120, 90, 150, 130]
})

print("📊 원본 데이터")
print(df)

df['이전_매출'] = df['매출'].shift(1)

print("\n📈 shift(1) 결과")
print(df)

